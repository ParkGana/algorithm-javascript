/* 런타임 에러 */
function solution(n, left, right) {
    let answer = [];

    for (let i = 1; i <= n; i++) {
        for (let j = 1; j <= n; j++) {
            answer.push(i > j ? i : j);
        }
    }

    return answer.slice(left, right + 1);
}

