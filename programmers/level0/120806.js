/* 두 수의 나눗셈 */

/**************************************************/

/* 정답 */
function solution(num1, num2) {
    return Math.floor((num1 / num2) * 1000);
}

/**************************************************/

/* 참고 */
function solution(num1, num2) {
    return Math.trunc((num1 / num2) * 1000);
}

