/* 정답 */
function solution(a, b) {
    let answer = 0;
    const days = ['THU', 'FRI', 'SAT', 'SUN', 'MON', 'TUE', 'WED'];
    const dates = [31, 29, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31];

    for (let i = 0; i < a - 1; i++) {
        answer += dates[i];
    }

    answer += b;

    return days[answer % 7];
}

